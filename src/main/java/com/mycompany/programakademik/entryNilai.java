    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            stat.executeUpdate("INSERT INTO nilai VALUES ("
                + "'" + vNoBp.getSelectedItem() + "',"
                + "'" + vKdMtk.getSelectedItem() + "',"
                + "'" + vNilaiTugas.getText() + "',"
                + "'" + vNilaiMid.getText() + "',"
                + "'" + vNilaiSemester.getText() + "',"
                + "'" + vNilaiAkhir.getText() + "',"
                + "'" + vNilaiHuruf.getText() + "'"
                + ")"
            );

            clean();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan...");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Keterangan Error : " + e);
        }

    }//GEN-LAST:event_saveActionPerformed

    private void processActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processActionPerformed
        Double totalTugas, totalMid, totalSemester, total;

        //nilai angka
        totalTugas = (Double.parseDouble(vNilaiTugas.getText()) * 20) / 100;
        totalMid = (Double.parseDouble(vNilaiMid.getText()) * 30) / 100;
        totalSemester = (Double.parseDouble(vNilaiSemester.getText()) * 50) / 100;

        total = totalTugas + totalMid + totalSemester;
        vNilaiAkhir.setText(String.valueOf(total));
        
        //nilai huruf
        if (total >= 80) {
            vNilaiHuruf.setText("A");
        } else if (total < 80 && total >= 65) {
            vNilaiHuruf.setText("B");
        } else if (total < 65 && total >= 55) {
            vNilaiHuruf.setText("C");
        } else if (total < 55 && total >= 40) {
            vNilaiHuruf.setText("D");
        } else {
            vNilaiHuruf.setText("E");
        }


    }//GEN-LAST:event_processActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        try {
            stat.executeUpdate("UPDATE nilai SET "
                + "nilaiTugas='" + vNilaiTugas.getText() + "', "
                + "nilaiMid='" + vNilaiMid.getText() + "', "
                + "nilaiSemester='" + vNilaiSemester.getText() + "', "
                + "nilaiAkhir='" + vNilaiAkhir.getText() + "', "
                + "nilaiHuruf='" + vNilaiHuruf.getText() + "' "
                + "WHERE noBp='" + vNoBp.getSelectedItem() + "' "
                + "AND kdMtk='" + vKdMtk.getSelectedItem() + "'"
            );
            clean();
            JOptionPane.showMessageDialog(rootPane, "Data berhasil Diupdate...");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

    }//GEN-LAST:event_editActionPerformed

