    private void vKdMtkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vKdMtkActionPerformed
        try {
            res = stat.executeQuery("SELECT * FROM matakuliah WHERE "
                + "kdMtk='" + vKdMtk.getSelectedItem() + "'"
            );
            while (res.next()) {
                vNamaMtk.setText(res.getString("namaMtk"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
        try {
            res = stat.executeQuery("SELECT * FROM nilai WHERE "
                + "noBp='" + vNoBp.getSelectedItem() + "' "
                + "AND kdMtk='" + vKdMtk.getSelectedItem() + "'"
            );

            while (res.next()) {
                vNilaiTugas.setText(res.getString("nilaiTugas"));
                vNilaiMid.setText(res.getString("nilaiMid"));
                vNilaiSemester.setText(res.getString("nilaiSemester"));
                vNilaiAkhir.setText(res.getString("nilaiAkhir"));
                vNilaiHuruf.setText(res.getString("nilaiHuruf"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

    }//GEN-LAST:event_vKdMtkActionPerformed

